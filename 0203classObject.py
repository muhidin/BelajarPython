class Hewan:
    def __init__(self, nama, jenis):
        self.nama = nama
        self.jenis = jenis

    

hewan1 = Hewan("Kucing", "Mamalia")
print("Nama Hewan: ",hewan1.nama)
print("Jenis Hewan: ",hewan1.jenis)

hewan2 = Hewan("Ayam", "Unggas")
print("Nama Hewan: ",hewan2.nama)
print("Jenis Hewan: ",hewan2.jenis)